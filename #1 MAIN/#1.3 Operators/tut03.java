//CONDITIONAL OR RELATIONAL OPERATORS

public class tut03{
    public static void main(String args[]){
      int a=100,b=50,c=100;

      System.out.println(a==b);
      System.out.println(a!=b);
      System.out.println(a>b);
      System.out.println(a<b);
      System.out.println(a>=c);
      System.out.println(a<=c);
    }
}