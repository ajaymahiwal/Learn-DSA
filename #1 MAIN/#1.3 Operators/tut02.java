//ASSIGNMENT OPERATORS

public class tut02{
    public static void main(String args[]){
        int x;
        x=5;
        System.out.println(x+=5);   // 5+5
        System.out.println(x-=5);   // 10-5
        System.out.println("");   

        int a=10;
        System.out.println(a*=10); // 10*10
        System.out.println(a/=5);  // 100/5
        System.out.println(a%=5);  // 25%5
        System.out.println("");

//BITWISE OPERATORS(TUT06) SHORTCUT USING ASSIGNMENT OPERATORS .... 
// &=	          x &= 3	          x = x & 3	
// |=	          x |= 3	          x = x | 3	
// ^=	          x ^= 3	          x = x ^ 3	
// >>=	          x >>= 3          	  x = x >> 3	
// <<=	          x <<= 3          	  x = x << 3

    }
}